# multiple function
def multiply(a,b):
    return a * b

# add function
def add(a,b):
    return a + b

# subtract function
def subtract(a,b):
    return a - b

# divide function
def divide(a,b):
    return a / b

# square function
def square(a):
    return a ** 2

# cube function
def cube(a):
    return a ** 3

# square_n function
# divide function
def square_n(a,n):
    return a ** n


print("I'm going use the calculator functions to multiply 5 and 6")
x = multiply(5,6)
print(x)

print("I'm going use the calculator functions to print square of 5")
x = square(5)
print(x)

print("I'm going use the calculator functions to print cube of 5")
x = cube(5)
print(x)

print("I'm going use the calculator functions to print square n of 5")
x = square_n(5,4)
print(x)


